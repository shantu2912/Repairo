// --- Localization Core Infrastructure ---
function changeLanguage(lang) {
    localStorage.setItem('fixzen_lang', lang);
    
    // 1. Process all basic elements containing data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    // 2. Refresh Alpine context variables where required
    window.dispatchEvent(new CustomEvent('lang-changed'));
}

// Global programmatic dictionary translation access logic
function t(key) {
    const lang = localStorage.getItem('fixzen_lang') || 'en';
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

// Ensure execution synchronization directly across initial page generation pipelines
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem('fixzen_lang') || 'en';
    changeLanguage(savedLang);
});
